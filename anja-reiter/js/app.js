function navToggle() {
    var mobilemenu = document.getElementById('mobilemenu');
    mobilemenu.classList.toggle('block');
    mobilemenu.classList.toggle('hidden');

    var mobilehamburger = document.getElementById('mobilehamburger');
    mobilehamburger.classList.toggle('block');
    mobilehamburger.classList.toggle('hidden');

    var mobileclose = document.getElementById('mobileclose');
    mobileclose.classList.toggle('block');
    mobileclose.classList.toggle('hidden');
}
